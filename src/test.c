void LED_GO(void)
{
	unsigned int i = 0,j = 0;
	P2 = 0xFF;
	
	while(1)
	{
		if(KEY == 0)	//红外接收管，判断起始位
		{
			j++;
			if(j > 10 * 16) 	//显示完15个字之后重新开始
			{
				j = 0;
			}
			
			for(i = j;i< 160+j;i++)
			{
				LED_SendDataC(love[i*2]);
				LED_SendDataB(love[i*2+1]);
				LED_SendDataA(0xFF);
				
				OEA=OEB=OEC=0;
                delayNus(170);	//延时让LED亮起来 每列延时的时间
                OEA=OEB=OEC=1;
			}
			
		}
	}
	
}

